const I18N = {
  current: 'zh-Hant',

  init() {
    const saved = localStorage.getItem('primacol_lang');
    if (saved && PRIMACOL_I18N[saved]) {
      this.current = saved;
    }
    this.apply();
    this.updateButtons();
  },

  set(lang) {
    if (!PRIMACOL_I18N[lang]) return;
    this.current = lang;
    localStorage.setItem('primacol_lang', lang);
    this.apply();
    this.updateButtons();
    document.dispatchEvent(new CustomEvent('langChanged', { detail: { lang } }));
  },

  t(key) {
    const keys = key.split('.');
    let val = PRIMACOL_I18N[this.current];
    for (const k of keys) {
      if (val && val[k] !== undefined) val = val[k];
      else return key;
    }
    return val;
  },

  apply() {
    document.documentElement.lang = this.current;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = this.t(key);
      if (text !== key) el.textContent = text;
    });

    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      const text = this.t(key);
      if (text !== key) el.innerHTML = text;
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const text = this.t(key);
      if (text !== key) el.placeholder = text;
    });

    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      const text = this.t(key);
      if (text !== key) el.title = text;
    });
  },

  updateButtons() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === this.current);
    });
  },
};

document.addEventListener('DOMContentLoaded', () => I18N.init());
